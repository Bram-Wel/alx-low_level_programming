/**
  * add - Adds 2 integers.
  * @a: Operand 1.
  * @b: Operand 2.
  *
  * Return: Sum of a & b.
  */
int add(int a, int b)
{
	return (a + b);
}

/**
  * sub - Subtracts 2 integers.
  * @a: Operand 1.
  * @b: Operand 2.
  *
  * Return: Difference of a & b.
  */
int sub(int a, int b)
{
	return (a - b);
}

/**
  * mul - Multiplies 2 integers.
  * @a: Operand 1.
  * @b: Operand 2.
  *
  * Return: Product of a & b.
  */
int mul(int a, int b)
{
	return (a * b);
}

/**
  * div - Divides 2 integers.
  * @a: Operand 1.
  * @b: Operand 2.
  *
  * Return: Quotient of a & b.
  */
int div(int a, int b)
{
	return (a / b);
}

/**
  * add - Modulus of 2 integers.
  * @a: Operand 1.
  * @b: Operand 2.
  *
  * Return: Mpdulus of a & b.
  */
int mod(int a, int b)
{
	return (a % b);
}

